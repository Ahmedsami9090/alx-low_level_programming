#include "3-calc.h"

/**
 * op_add - entry point
 * @a: input
 * @b: input
 * Return: sum of inputs
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - entry point
 * @a: input
 * @b: input
 * Return: sub of inputs
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - entry point
 * @a: input
 * @b: input
 * Return: mul of inputs
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - entry point
 * @a: input
 * @b: input
 * Return: div of inputs
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - entry point
 * @a: input
 * @b: input
 * Return: mod of inputs
*/

int op_mod(int a, int b)
{
	return (a % b);
}
